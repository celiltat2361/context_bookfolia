class BookList extends React.Component {
  static contextType = BookContext;

  render() {

    return (
      <ThemeContext.Consumer>
        {(contextTheme) => (
          <BookContext.Consumer>
            { contextBook => {
            const { books } = contextBook;  
            const { isDarkTheme, dark, light, changeTheme } = contextTheme;
            const theme = isDarkTheme ? dark : light;
          
            return (
              <section className="page-section" style={{background: theme.bg, color: theme.txt}} id="portfolio">
                <div className="container">
                  <div className="text-center">
                    <h2 className="section-heading text-uppercase" style={{color: 'red'}}>My Books</h2>
                    <h3 className="section-subheading text-muted">
                      Lorem ipsum dolor sit amet consectetur.
                    </h3>
                    <button type="button" className="btn btn-sm btn-info mb-5" onClick={changeTheme}>Change Theme</button>
                  </div>
                  <div className="row">
                    {books.map((book, i) => {
                      return <Book book={book} key={i} />;
                    })}
                  </div>
                </div>
              </section>
            )
          }}
        </BookContext.Consumer>
        )}
      </ThemeContext.Consumer>
     
    );
  }
}

